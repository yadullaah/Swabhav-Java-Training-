package com.training;

public class VowelSwitchCases {

	public static void main(String[] args) {

		char ch = 'a';
		switch (ch) {

		case 'a':
			System.out.println("It is a vowel");
			break;

		case 'e':
			System.out.println("It is a vowel");
			break;

		case 'i':
			System.out.println("It is a vowel");
			break;

		case 'o':
			System.out.println("It is a vowel");
			break;

		case 'u':
			System.out.println("It is a vowel");
			break;
			
		default:
			System.out.println("It is not a vowel");
		}

	}
}
